        <h1 id="introduction">Introduction</h1>
<p>RESTful API for managing loans, borrowers, payments, EMI schedules, and reports. Supports multi-tenancy, role-based access control, partial payments, foreclosure, audit logging, and in-app notifications.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost</code>
</aside>
<pre><code>This documentation aims to provide all the information you need to work with our API.

&lt;aside&gt;As you scroll, you'll see code examples for working with the API in different programming languages in the dark area to the right (or as part of the content on mobile).
You can switch the language used with the tabs at the top right (or from the nav menu at the top left on mobile).&lt;/aside&gt;</code></pre>

        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {YOUR_AUTH_KEY}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>This API uses Laravel Sanctum for authentication. Obtain a token by sending a POST request to <code>/api/login</code> with valid credentials. Include the token in the <code>Authorization</code> header as <code>Bearer {token}</code>.</p>

